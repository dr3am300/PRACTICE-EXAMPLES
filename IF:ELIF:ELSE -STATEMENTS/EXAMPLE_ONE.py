# EXAMPLE 1: Write a program that prompts the user to input the color of a traffic light (red, yellow,green) and outputs the action a driver should take.

light_color = input("Enter the color of the traffic light(red, yellow, green): ")
if light_color == "red":
    print("Stop the car")   
elif light_color == "yellow":
    print("Slow down")
else:
    print("Go")








